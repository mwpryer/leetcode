// @lc code=start
/**
 * @param {number[]} nums1
 * @param {number[]} nums2
 * @return {number[]}
 */
var intersection = function (nums1, nums2) {
  const res = []
  const dict = {}
  for (const num of nums1) {
    dict[num] = true
  }
  for (const num of nums2) {
    if (dict[num]) {
      res.push(num)
      dict[num] = false
    }
  }
  return res
}
