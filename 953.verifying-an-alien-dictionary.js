// @lc code=start
/**
 * @param {string[]} words
 * @param {string} order
 * @return {boolean}
 */
var isAlienSorted = function (words, order) {
  const map = {}
  for (let i = 0; i < order.length; i++) {
    map[order[i]] = i
  }

  for (let i = 1; i < words.length; i++) {
    const word1 = words[i - 1]
    const word2 = words[i]
    for (let j = 0; j < word1.length; j++) {
      if (j >= word2.length) return false
      if (word1[j] !== word2[j]) {
        if (map[word1[j]] > map[word2[j]]) return false
        break
      }
    }
  }
  return true
}
